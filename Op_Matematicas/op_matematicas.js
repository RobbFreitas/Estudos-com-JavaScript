// + Adição
// - Subtração
// * Multiplicação
// / Divisão
// % Módulo
// ++ Incremento
// -- Decremento

var num1 = 2;
var num2 = 4;

// Adição
var adicao = 2 + 4;
console.log(adicao) // 6

// Subtração
var subtracao = 4 - 2;
console.log(subtracao) // 2

// Multiplicação
var multpicacao = 2 * 4;
console.log(multpicacao) // 8

// Divisão
var divisao = 4 / 2;
console.log(divisao); // 2

// Módulo
var modulo = 4 % 2;
console.log(modulo);

// Incremento

num1 = num1 + 1; // ou num1++ num1 +=
console.log(num1); // 3

// Decremento

num2 = num2 - 1;
console.log(num2); // 3
